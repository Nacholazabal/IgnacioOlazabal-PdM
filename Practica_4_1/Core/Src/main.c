/* USER CODE BEGIN 0 */
bool_t readButton(void) {
	bool_t pin_state = HAL_GPIO_ReadPin(BUTTON_PORT, BUTTON_PIN);
    // If active-low, invert logic
    return BUTTON_PULL_LOW ? !pin_state : pin_state;
}

static void on_button_released(void){
    time_select = (time_select + 1) % t_size;
    delayWrite(&delay, time_array[time_select]);
}

void debounceFSM_init(void){
	current_state = BUTTON_UP;
	delayInit(&delay_debounce, DEBOUNCE_PERIOD_MS);
}

void debounceFSM_update(void){
	bool_t button_status = readButton();
	switch(current_state){
		case BUTTON_UP:
			if(button_status == BUTTON_PRESSED){
				current_state = BUTTON_FALLING;
				delayInit(&delay_debounce, DEBOUNCE_PERIOD_MS);
			}
			break;
		case BUTTON_FALLING:
			if(delayRead(&delay_debounce)){
				if(button_status == BUTTON_PRESSED){
					//on_button_pressed();
					// nothing happens when button is pressed, only when released
					current_state = BUTTON_DOWN;
				}else{
					current_state = BUTTON_UP;
				}
			}
			break;
		case BUTTON_DOWN:
			if(button_status == BUTTON_RELEASED){
				current_state = BUTTON_RAISING;
				delayInit(&delay_debounce, DEBOUNCE_PERIOD_MS);
			}
			break;
		case BUTTON_RAISING:
			if(delayRead(&delay_debounce)){
				if(button_status == BUTTON_RELEASED){
					on_button_released();
					current_state = BUTTON_UP;
				}else{
					current_state = BUTTON_DOWN;
				}
			}
			break;
		default:
			debounceFSM_init();
		break;
	}
}

